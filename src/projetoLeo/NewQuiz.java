         InputVerifier linkV = new FieldValidator(link, "Link obrigatório");
         InputVerifier tipV = new FieldValidator(tip, "Dica obrigatória");
         InputVerifier tagsV = new FieldValidator(tags, "Tags obrigatórias");
         
         InputVerifier quest1V = new FieldValidator(pergunta1, "Pergunta obrigatória");
         InputVerifier alternA1V = new FieldValidator(alternA1, "Alternativa obrigatória");
         InputVerifier alternB1V = new FieldValidator(alternB1, "Alternativa obrigatória");
         InputVerifier alternC1V = new FieldValidator(alternC1, "Alternativa obrigatória");
         InputVerifier alternD1V = new FieldValidator(alternD1, "Alternativa obrigatória");
         
         InputVerifier quest2V = new FieldValidator(pergunta2, "Pergunta obrigatória");
         InputVerifier alternA2V = new FieldValidator(alternA2, "Alternativa obrigatória");
         InputVerifier alternB2V = new FieldValidator(alternB2, "Alternativa obrigatória");
         InputVerifier alternC2V = new FieldValidator(alternC2, "Alternativa obrigatória");
         InputVerifier alternD2V = new FieldValidator(alternD2, "Alternativa obrigatória");
         
         InputVerifier quest3V = new FieldValidator(pergunta3, "Pergunta obrigatórias");
         InputVerifier alternA3V = new FieldValidator(alternA3, "Alternativa obrigatória");
         InputVerifier alternB3V = new FieldValidator(alternB3, "Alternativa obrigatória");
         InputVerifier alternC3V = new FieldValidator(alternC3, "Alternativa obrigatória");
         InputVerifier alternD3V = new FieldValidator(alternD3, "Alternativa obrigatória");
         
         boolean categoriaB = categoriaV.verify(categoria);
         boolean nameB = nameV.verify(name);
         boolean descB = descV.verify(description);
         boolean linkB = linkV.verify(link);
         boolean tipB = tipV.verify(tip);
         boolean tagsB = tagsV.verify(tags);
         
         boolean quest1B = quest1V.verify(pergunta1);
         boolean alternA1B = alternA1V.verify(alternA1);
         boolean alternB1B = alternB1V.verify(alternB1);
         boolean alternC1B = alternC1V.verify(alternC1);
         boolean alternD1B = alternD1V.verify(alternD1);
         
         boolean quest2B = quest2V.verify(pergunta2);
         boolean alternA2B = alternA2V.verify(alternA2);
         boolean alternB2B = alternB2V.verify(alternB2);
         boolean alternC2B = alternC2V.verify(alternC2);
         boolean alternD2B = alternD2V.verify(alternD2);
         
         boolean quest3B = quest3V.verify(pergunta3);
         boolean alternA3B = alternA3V.verify(alternA3);
         boolean alternB3B = alternB3V.verify(alternB3);
         boolean alternC3B = alternC3V.verify(alternC3);
         boolean alternD3B = alternD3V.verify(alternD3);
         
         if (categoriaB && nameB && descB && linkB && tipB && tagsB && quest1B && alternA1B && alternB1B && alternC1B && alternD1B && quest2B && alternA2B && alternB2B && alternC2B && alternD2B && quest3B && alternA3B && alternB3B && alternC3B && alternD3B) {
             this.setVisible(false);
             new TelaQuiz().setVisible(true);
         }
